     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        // dd($request->all());
        $request->validate([
            'name'=>'required|string|alpha',
            'slug'=>'required|string|alpha',
            'is_active'=>'nullable'
        ]);
        // dd($request->all());
        category::create([
            'name'=>$request->name,
            'slug'=>$request->slug,
            'is_active'=>$request->filled('is_active'),
        ]);
        return back();
    }

